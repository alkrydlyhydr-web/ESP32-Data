# ملخص
# يُقدّم هذا المشروع نظامًا محمولًا لحضور الحضور باستخدام بصمات الأصابع، قائمًا على تقنية إنترنت الأشياء، ومُصمّم خصيصًا للمؤسسات التعليمية. يستخدم النظام وحدة التحكم الدقيقة ESP32 للتفاعل مع مستشعر بصمات الأصابع AS608، وشاشة LCD بحجم 16x2، وأزرارًا للتفاعل مع المستخدم. تُشفّر بيانات الحضور باستخدام خوارزمية AES-128، وتُخزّن في قاعدة بيانات MySQL، وتُعالج بواسطة برنامج Python لإنشاء تقارير شاملة. يُلغي النظام الحاجة إلى تتبع الحضور يدويًا، ويُقلّل من مخاطر التلاعب، ويضمن سلامة البيانات. سمات
# المصادقة البيومترية : تسجيل حضور آمن قائم على بصمات الأصابع باستخدام مستشعر AS608.
# التتبع في الوقت الفعلي : يقوم بتحميل البيانات المشفرة إلى MySQL للوصول الفوري إليها.
# واجهة سهلة الاستخدام : شاشة LCD 16x2 وأزرار ضغط لاختيار الفصول الدراسية والتسجيل والحذف.
# أمن البيانات : تشفير AES-128 لسجلات الحضور.
# دعم وضع عدم الاتصال : يقوم بتخزين البيانات في ذاكرة EEPROM أثناء انقطاع شبكة الواي فاي.
# تقارير متعددة التنسيقات : يقوم بإنشاء تقارير بتنسيقات CSV وHTML وExcel باستخدام لغة بايثون.



